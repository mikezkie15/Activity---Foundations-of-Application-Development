function addNumber(x, y) {
  console.log(`${x} + ${y} = ${x + y}`);
}

function subtractNumber(x, y) {
  console.log(`${x} - ${y} = ${x - y}`);
}

function multiplyNumber(x, y) {
  console.log(`${x} x ${y} = ${x * y}`);
}

function divisionNumber(x, y) {
  console.log(`${x} / ${y} = ${x / y}`);
}

